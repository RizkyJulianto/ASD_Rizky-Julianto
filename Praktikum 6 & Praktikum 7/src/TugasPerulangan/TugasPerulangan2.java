public class TugasPerulangan2 {
    public static void main(String[] args) {
        
//Inisialisasi variabel angka
        int angka = 11;
        
//kode perulangan while
        while(angka < 10) {
//Perintah cetak yang akan terus dijalankan apabila kondisi bernilai true
            System.out.println(angka);
//Perintah untuk menambahkan nilai angka dengan 1 
            angka++;
        }
    }
}
