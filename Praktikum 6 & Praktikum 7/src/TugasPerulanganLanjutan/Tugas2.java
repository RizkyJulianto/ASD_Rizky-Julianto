public class Tugas2 {
    public static void main(String[] args) {
        
//      Buat variabel angka yang tipe datanya integer yang isinya adalah 1
        int angka = 1;
        
        do {
            System.out.println(angka); //Perintah untuk mencetak nilai variabel angka
            angka++; //Kode untuk menambahkan 1 pada nilai variabel angka
        } while(angka <= 10); //Kondisi dimana kapan perulangan akan berakhir
    }
}
